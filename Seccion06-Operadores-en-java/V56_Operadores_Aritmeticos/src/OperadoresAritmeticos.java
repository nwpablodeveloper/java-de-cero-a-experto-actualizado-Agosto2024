public class OperadoresAritmeticos {
    public static void main(String[] args) {
        System.out.println("*** Operadores Aritméticos ***");

        double a = 5, b = 3, resultado;

        // Suma +
        resultado = a + b;
        System.out.println("Resultado de suma = " + resultado);

        // Resta -
        resultado = a - b;
        System.out.println("Resultado de resta = " + resultado);

        // Multiplicación *
        resultado = a * b;
        System.out.println("Resultado de multiplicación = " + resultado);

        // Multiplicación *
        resultado = a * b;
        System.out.println("Resultado de multiplicación = " + resultado);

        // División /
        resultado = a / b;
        System.out.println("Resultado de la división = " + resultado);

        // Módulao ( residuo de la división ) %
        resultado = a % b;
        System.out.println("Resultado módulo = " + resultado);




    }
}
