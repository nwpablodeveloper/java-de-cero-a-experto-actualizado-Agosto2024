public class OperadorNOT {
    public static void main(String[] args) {
        System.out.println("\n*** Operador Lógico NOT ***");
        
        boolean a = false;
        System.out.println("a = " + a);
        
        // Operador not ! invierte el valor lógico
        boolean resultado = !a;
        System.out.println("Invertir valor de a = " + resultado);
    }
}
